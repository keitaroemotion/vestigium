def make_insert_sentence(lines, schema, scheme_id, i)
  begin
    f = File.open("#{$sqldir}/#{scheme_id}#{i}", "w")
    lines.each do |line|
      if line == nil
        next
      end
      token = line.split(' ')
      schema.keys.each do |ind|
        if ind == -1 #concat date
          args = schema[ind].split(' ')
          f.print "#{token[args[2].to_i]} #{token[args[3].to_i].split(',')[0]}\t"
        else
          f.print "#{token[ind]}\t"
        end
      end
      f.puts
    end
    f.close
    rescue
    end
end



def ret(lines, s, num)
  tail = s+$UNIT*(num+1)-1
  if tail > lines.size-1
    tail = lines.size-1
  end
  [lines[s+$UNIT*num..tail], "#{num}_#{s}"]
end


